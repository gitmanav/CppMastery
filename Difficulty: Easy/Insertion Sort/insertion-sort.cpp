class Solution {
  public:
    // Please change the array in-place
    void insertionSort(vector<int>& arr) {
        // code here
        for(int j=1;j<arr.size();j++)
        {
            int i=j-1;
            int key=arr[j];
            while(i>=0 && arr[i]>key)
            {
                arr[i+1]=arr[i];
                i=i-1;
            }
            arr[i+1]=key;
        }
    }
};

